#List methods
#Sorting
a = [4,56,45,41,5,5,8,5]
a.sort()
print(a)
#Reversing 
a.reverse()
print(a)
#appends-Used to add something to a list.
a.append(81)
a.append(78)
a.append(100)
print(a)
#insert-Used to insert a object at any index.
a.insert(3,"Hello")
print(a)
#pop-Used to delete an object at given position
a.pop(3)
print(a)
#remove-Used to remove any object.
a.remove(81)
print(a)

print(a)
